#include<iostream>
using namespace std;
int main(){
    int a=10;
    float b=3.14;
    double c=9.99;
    char d='A';
    bool e=true;
    cout<<a<<endl;
    cout<<b<<endl;
    cout<<c<<endl;
    cout<<d<<endl;
    cout<<e<<endl;
    cout<<"SIZEOF a="<<sizeof(a)<<endl;
    cout<<"SIZEOF b="<<sizeof(b)<<endl;
    cout<<"SIZEOF c="<<sizeof(c)<<endl;
    cout<<"SIZEOF d="<<sizeof(d)<<endl;
    cout<<"SIZEOF e="<<sizeof(e)<<endl;
    return 0;
}
