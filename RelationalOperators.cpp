#include<iostream>
using namespace std;
int main(){
    int a,b;
    cout<<"Enter two number:"<<endl;
    cin>>a>>b;
    cout<<"a==b:"<<(a==b)<<endl;
    cout<<"a!=b:"<<(a!=b)<<endl;
    cout<<"a>b:"<<(a>b)<<endl;
    cout<<"a<b:"<<(a<b)<<endl;
    cout<<"a>=b:"<<(a>=b)<<endl;
    cout<<"a<=b:"<<(a<=b)<<endl;
}
